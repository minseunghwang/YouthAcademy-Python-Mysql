# 리터럴
# 프로그램 작성 시 코드에 작성하는 값을 의미한다.
# 리터럴도 CPU가 사용하기 위해 기억공간에 저장된다.

print('정수 리터럴 : ', 100)
print('실수 리터럴 : ', 11.11)
print('논리형 리터럴 : ', True)
print('논리형 리터럴 : ', False)
print('문자열 리터럴 : ', '문자열 입니다.')
print('문자열 리터럴 : ', "문자열 입니다.")

# type 함수 : 작성한 값의 타입을 파악할 수 있다.
print('정수 타입 :', type(100))
print('실수 타입 :', type(11.11))
print('논리형 타입 :', type(True))
print('문자열 타입 :', type('문자열'))