# 정수 자료형
a1 = 100
print('a1 :', a1)
print('a1 type :', type(a1))

# 복소수
a2 = 10 + 3j
print('a2 :', a2)
print('a2 type :', type(a2))

# 실수
a3 = 11.11
print('a3 :', a3)
print('a3 type :', type(a3))

# 논리형
a4 = True
