# 세미콜론
# 파이썬 세미콜론은 선택.
print('명령어1')
print('명령어2');

# 한줄에 여러 명령문을 작성할 때는 세미콜론을 찍어준다.
print('명령어3'); print('명령어4'); print('명령어5')

# 출력문
# print 함수는 출력 후 밑으로 내려준다.
print('출력문')

# 문자열끼리 더하기 연산을 하면 하나의 문자열로 합쳐진다.
print('문자열1' + '문자열2')

# 문자열과 문자열 아닌 것을 더하면 오류가 발생한다.
# print('문자열1' + 100)

# 여러 형태의 값을 동시에 출력하고자 한다면 쉼표로 구분한다.
